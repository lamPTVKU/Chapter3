

// Write an application that prints all of integer the numbers between 1 (included) and 100(included) using "for" loop

package exercise6chapter3;

public class Exercise6Chapter3 {

    public static void main(String[] args) {
        for (int i = 2; i <= 100; i++) {
            System.out.println(i);
            i++;
        }
    }
    
}
