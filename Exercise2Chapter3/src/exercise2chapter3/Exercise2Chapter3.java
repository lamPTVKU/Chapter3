
// Write an application that prints all of integer the numbers between 1 (included) and 100(included) using "do..while" loop

package exercise2chapter3;


public class Exercise2Chapter3 {
    public static void main(String[] args) {
        int  i = 1;
        do {
            System.out.println(i);
            i++;
        }while(i<=100);
    }
    
}
