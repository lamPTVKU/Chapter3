// Write an application that prints all of the even numbers between 1 (included) and 100(included) using "for" loop

package exercise4chapter3;


public class Exercise4Chapter3 {
    public static void main(String[] args) {
        int i = 2;
        while(i<=100) {
            System.out.println(i);
            i+=2;
        }
    }
    
}
