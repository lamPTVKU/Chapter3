

// Write an application that prints all of integer the numbers between 1 (included) and 100(included) using "while" loop

package exercise1chapter3;

public class Exercise1Chapter3 {
    public static void main(String[] args) {
        int  i = 1;
        while(i<=100) {
            System.out.println(i);
            i++;
        }
    }
    
}
