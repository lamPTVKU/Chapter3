// Write an application that prints all of integer the numbers between 1 (included) and 100(included) using "do..while" loop

package exercise5chapter3;

public class Exercise5Chapter3 {
    public static void main(String[] args) {
        int i = 2;
        do {
            System.out.println(i);
            i+=2;
        } while (i<=100);
    }
    
}
